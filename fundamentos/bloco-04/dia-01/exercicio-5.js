const alpha = 180;
const beta = 60;
const gama = 60;
const angle = alpha + beta + gama;

if (alpha >= 180 || beta >= 180 || gama >= 180) {
    console.log('um triângulo não pode ter um ângulo maior ou igual a 180°');
} else if (alpha < 0 || beta < 0 || gama < 0) {
    console.log('não existe ângulo negativo em um triângulo');
} else if (angle === 180){
    console.log('true');
} else if (angle < 180 || angle > 180) {
    console.log('false');
}

